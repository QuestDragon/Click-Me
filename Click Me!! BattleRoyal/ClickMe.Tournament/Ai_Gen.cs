        public static List<string> Ai_Name(int range) //使える範囲 呼び出すならStaticを追加 Returnで返す型、ない場合はvoid。 メソッド名(あれば引数の型 引数の変数名)
        {
            var names = name_gen(); //リストをロード
            var results = new List<string>(); //結果用リスト

            //for文： for(カウント用の型 カウント用の変数名 = カウント用の変数に入れる初期値; 条件式; カウント方法※インクリメントなら「変数名++」、デクリメントなら「変数名--」)
            for(int i = 1; i <= range; i++)
            {
                Random random = new Random();
                int rnd = random.Next(names.Count);

                //ランダム抽出した名前がすでにResultsのリストに含まれるかインデックス番号を検索する。
                if (0 < results.IndexOf(names[rnd]) + 1) //返ってきた数字に+1して0より大きければ存在する。存在しない場合は-1が帰るため+1して0になる。0の場合はResultsのリストに追加。
                {
                    i -= 1; //iを減らしてもう1回引き直す
                }
                else
                {
                    results.Add(names[rnd]); //ランダム抽出した名前をリストに格納
                }
                    
            }

            return results; //結果用リストを戻り値として返す。
        }

